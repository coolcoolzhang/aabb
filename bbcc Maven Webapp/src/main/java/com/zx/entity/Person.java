package com.zx.entity;



public class Person {
	private int pid;
	private String pname;
	private String pse;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPse() {
		return pse;
	}
	public void setPse(String pse) {
		this.pse = pse;
	}
	

	
}
